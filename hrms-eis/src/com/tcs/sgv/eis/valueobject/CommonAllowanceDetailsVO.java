package com.tcs.sgv.eis.valueobject;

public class CommonAllowanceDetailsVO
{
	private long allowCode;
	
	private String allowName;
	
	private String allowDesc;
	
	private String edpCode;

	public long getAllowCode()
	{
		return allowCode;
	}

	public void setAllowCode(long allowCode)
	{
		this.allowCode = allowCode;
	}

	public String getAllowName()
	{
		return allowName;
	}

	public void setAllowName(String allowName)
	{
		this.allowName = allowName;
	}

	public String getAllowDesc()
	{
		return allowDesc;
	}

	public void setAllowDesc(String allowDesc)
	{
		this.allowDesc = allowDesc;
	}

	public String getEdpCode()
	{
		return edpCode;
	}

	public void setEdpCode(String edpCode)
	{
		this.edpCode = edpCode;
	}
}
