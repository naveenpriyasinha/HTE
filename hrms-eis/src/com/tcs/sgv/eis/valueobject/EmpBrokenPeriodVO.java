package com.tcs.sgv.eis.valueobject;

public class EmpBrokenPeriodVO {
	private long empId;
	private long brokenPeriodId;
	private long basicAmount;
	/**
	 * @return the empId
	 */
	public long getEmpId() {
		return empId;
	}
	/**
	 * @param empId the empId to set
	 */
	public void setEmpId(long empId) {
		this.empId = empId;
	}
	/**
	 * @return the brokenPeriodId
	 */
	public long getBrokenPeriodId() {
		return brokenPeriodId;
	}
	/**
	 * @param brokenPeriodId the brokenPeriodId to set
	 */
	public void setBrokenPeriodId(long brokenPeriodId) {
		this.brokenPeriodId = brokenPeriodId;
	}
	/**
	 * @return the basicAmount
	 */
	public long getBasicAmount() {
		return basicAmount;
	}
	/**
	 * @param basicAmount the basicAmount to set
	 */
	public void setBasicAmount(long basicAmount) {
		this.basicAmount = basicAmount;
	}
	
	
}
