package com.tcs.sgv.eis.service;

public class CustomDeduction 
{
	private long employeeId = 0;
	private String employeeName = "";
	private Double amount = 0.0;
	private String insertFlag = "";
	//added by vaibhav tyagi: start
	private String nonGovTypeValue = "";
	//added by vaibhav tyagi: end
	
	/**
	 * @return the employeeName
	 */
	public String getEmployeeName() {
		return employeeName;
	}
	/**
	 * @return the insertFlag
	 */
	public String getInsertFlag() {
		return insertFlag;
	}
	/**
	 * @param insertFlag the insertFlag to set
	 */
	public void setInsertFlag(String insertFlag) {
		this.insertFlag = insertFlag;
	}
	/**
	 * @param employeeName the employeeName to set
	 */
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	/**
	 * @return the amount
	 */
	public Double getAmount() {
		return amount;
	}
	/**
	 * @param amount the amount to set
	 */
	public void setAmount(Double amount) {
		this.amount = amount;
	}
	
	public CustomDeduction()
	{
		
	}
	public CustomDeduction(String employeeName, Double amount, long employeeId,String nonGovTypeValue)
	{
		this.employeeName = employeeName;
		this.amount = amount;
		this.employeeId = employeeId;
		this.nonGovTypeValue = nonGovTypeValue;
	}
	/**
	 * @return the employeeId
	 */
	public long getEmployeeId() {
		return employeeId;
	}
	/**
	 * @param employeeId the employeeId to set
	 */
	
	//added by vaibhav tyagi: start
	public void setEmployeeId(long employeeId) {
		this.employeeId = employeeId;
	}
	public void setNonGovTypeValue(String nonGovTypeValue) {
		this.nonGovTypeValue = nonGovTypeValue;
	}
	public String getNonGovTypeValue() {
		return nonGovTypeValue;
	}
	//added by vaibhav tyagi: end
}