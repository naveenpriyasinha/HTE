function HideEduTab()
{	
	if (document.getElementById("expandExpEduTable") != null)
	{
		var imgSrcPath = new String(document.getElementById("expandExpEduTable").src);
		
		if (imgSrcPath.indexOf("expand.gif") != -1)
		{
			document.getElementById("expandExpEduTable").src = imgSrcPath.replace("expand.gif","collapse.gif");
			document.getElementById('EduFileTable').style.display='';
			document.getElementById('LangFileTable').style.display='';
		}
		else
		{
			document.getElementById("expandExpEduTable").src = imgSrcPath.replace("collapse.gif","expand.gif");
			document.getElementById('EduFileTable').style.display='none';
			document.getElementById('LangFileTable').style.display='none';	
			document.getElementById("expandExpLangFile").src = imgSrcPath.replace("collapse.gif","expand.gif");
			document.getElementById("expandExpEduFile").src = imgSrcPath.replace("collapse.gif","expand.gif");
			document.getElementById('LangFile').style.display='none';
			document.getElementById('EduFile').style.display='none';						
		}
	}			
}
