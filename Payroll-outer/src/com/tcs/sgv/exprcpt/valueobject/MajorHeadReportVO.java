package com.tcs.sgv.exprcpt.valueobject;


public class MajorHeadReportVO implements java.io.Serializable
{
	private int count;
	private String majorHead;
	private String majorhdDesc;
	private String vouchers;
	private String totalAmount;
	                           
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getMajorhdDesc() {
		return majorhdDesc;
	}
	public void setMajorhdDesc(String majorhdDesc) {
		this.majorhdDesc = majorhdDesc;
	}
	public String getMajorHead() {
		return majorHead;
	}
	public void setMajorHead(String majorHead) {
		this.majorHead = majorHead;
	}
	public String getTotal() {
		return totalAmount;
	}
	public void setTotal(String total) {
		this.totalAmount = total;
	}
	public String getVouchers() {
		return vouchers;
	}
	public void setVouchers(String vouchers) {
		this.vouchers = vouchers;
	}
}